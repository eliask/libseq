inline int is_prime(UL_int b)
{
  L_int i,tmp;

  if(b==0) return 0;
  if(b==1) return 0;
  if(b==2) return 1;
  if(b==3) return 1;

  i=2;
  tmp=(UL_int)sqrt((double)b)+1;

  while(i<=tmp) 
    {
      if((b%i)==0) return(0);
      i++;
    }
  return(1);
}

