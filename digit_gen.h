////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////

inline L_int int_log(L_int a, UL_int b)
{
  L_int i,tmp;

  tmp=a;
  i=0;
  while(tmp>1) 
    {
      tmp=tmp/b;
      i++;
    }
  return(i);
}

